#include <iostream>
#include<cmath>
using namespace std;
int main()
{
	setlocale(LC_ALL, "");
	double num_x;
	cout << "Введите любое число:";
	cin >> num_x;


	if (num_x == 2);
	cout << "Ответ:" << ((cos(3) * num_x + pow(num_x, 3)) / (pow(sin(num_x), 3) * num_x - 3 * num_x));
	if (num_x > 2);
	cout << "Ответ:" << (pow(sin(num_x), 3) + sqrt(pow(num_x, 2) + 2));
	if (num_x < 2);
	cout << "Ответ:" << ((pow(num_x, 3)) * (log(3 / 2)) * (abs(atan(num_x) - exp(-num_x))));
}